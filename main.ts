sprites.onOverlap(SpriteKind.Player, SpriteKind.Enemy, function (sprite, otherSprite) {
    count += 1
    if (count == 10 + level) {
        level += 1
        music.jumpUp.play()
        startLevel()
    } else {
        music.baDing.play()
    }
    info.changeScoreBy(1)
    otherSprite.startEffect(effects.halo, 200)
    for (let index = 0; index < 1; index++) {
        pause(200)
        otherSprite.destroy()
    }
})
